import { notFound, redirect } from "next/navigation";

import DashboardStats from "@/components/dashboard/DashboardStats";
import { getAuthSession } from "@/lib/auth";
import { getDashboardUser } from "@/lib/mock-data";
import { formatDate, formatDateTime } from "@/lib/utils";

type DashboardPageProps = {
  params: Promise<{ username: string }>;
};

export default async function DashboardPage({ params }: DashboardPageProps) {
  const { username } = await params;
  const session = await getAuthSession();

  if (!session?.user) {
    redirect("/auth/login");
  }

  const sessionUsername = session.user.username;
  if (sessionUsername.toLowerCase() !== username.toLowerCase()) {
    redirect(`/${sessionUsername}/dashboard`);
  }

  const user = getDashboardUser(username);
  if (!user) {
    notFound();
  }

  return (
    <section className="mx-auto w-full max-w-6xl px-4 py-10 sm:px-6">
      <div className="mb-8 rounded-xl border border-[var(--border)] bg-[var(--surface)] p-6">
        <p className="text-xs uppercase tracking-wide text-[var(--muted-foreground)]">User Dashboard</p>
        <h1 className="mt-2 text-3xl font-semibold text-[var(--foreground)]">@{user.username}</h1>
        <div className="mt-3 flex flex-wrap gap-4 text-sm text-[var(--muted-foreground)]">
          <span>Join date: {formatDate(user.joinDate)}</span>
          <span>Rank: {user.rank ?? "Unranked"}</span>
        </div>
      </div>

      <DashboardStats stats={user.stats} />

      <div className="mt-8 rounded-xl border border-[var(--border)] bg-[var(--surface)] p-6">
        <h2 className="text-xl font-semibold text-[var(--foreground)]">Recent activity</h2>
        {user.recentActivity.length === 0 ? (
          <p className="mt-3 text-sm text-[var(--muted-foreground)]">No solved problems yet. Start with an easy contest problem.</p>
        ) : (
          <ul className="mt-4 space-y-3">
            {user.recentActivity.map((activity) => (
              <li key={activity.id} className="rounded-lg border border-[var(--border)] bg-[var(--surface-2)] p-4">
                <div className="flex flex-wrap items-center justify-between gap-2">
                  <p className="font-medium text-[var(--foreground)]">{activity.title}</p>
                  <span className="text-xs text-[var(--muted-foreground)]">{formatDateTime(activity.solvedAt)}</span>
                </div>
                <p className="mt-1 text-sm text-[var(--muted-foreground)]">Difficulty: {activity.difficulty}</p>
              </li>
            ))}
          </ul>
        )}
      </div>
    </section>
  );
}
